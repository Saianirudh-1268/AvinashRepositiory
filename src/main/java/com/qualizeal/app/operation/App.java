package com.qualizeal.app.operation;

public class App {
	public int a=20;
	public int b=10;
    public int add() {
    	int result = a + b;
    	return result;
    }
    public int sub() {
    	int result = a - b;
    	return result;
    }
    public int mul() {
    	int result = a * b;
    	return result;
    }
    public int div() {
    	int result = a / b;
    	return result;
    }
}
